package net.neoturbine.autolycus.internal;

import java.util.Date;

public class Prediction {
	private final int predtype;
	private final String stopName;
	private final int stpid; //stop id
	private final int vid; //vehicle id
	private final int distance; //distance in feet
	private final String rt; //Route
	private final String dir; //Direction
	private final String des; //destination
	private final boolean delayed;
	
	private final Date estTime; //estimated time
	private final Date predTime; //when prediction was generated
	
	
	//constants for predtype
	public static final int ARRIVAL_PREDICTION = 1;
	public static final int DEPARTURE_PREDICTION = 2;
	
	//constructors, first generated by eclipse
	public Prediction(int predtype, String stopName, int stpid, int vid,
			int distance, String rt, String dir, String des, Date estTime,
			Date predTime, Boolean delayed) {
		this.predtype = predtype;
		this.stopName = stopName;
		this.stpid = stpid;
		this.vid = vid;
		this.distance = distance;
		this.rt = rt;
		this.dir = dir;
		this.des = des;
		this.estTime = estTime;
		this.predTime = predTime;
		this.delayed = delayed;
	}
	
	//Generated getters
	public int getType() {
		return predtype;
	}
	public String getStopName() {
		return stopName;
	}
	public int getStopID() {
		return stpid;
	}
	public int getVid() {
		return vid;
	}
	public int getDistance() {
		return distance;
	}
	public String getRoute() {
		return rt;
	}
	public String getDirection() {
		return dir;
	}
	public String getDestination() {
		return des;
	}
	public Date getEstTime() {
		return estTime;
	}
	public Date getPredTime() {
		return predTime;
	}
	public boolean isDelayed() {
		return delayed;
	}
	//generated by eclipse
	@Override
	public String toString() {
		return String
				.format(
						"Prediction [getDestination()=%s, getDirection()=%s, getDistance()=%s, getEstTime()=%s, getPredTime()=%s, getRoute()=%s, getStopID()=%s, getStopName()=%s, getType()=%s, getVidID()=%s]",
						getDestination(), getDirection(), getDistance(),
						getEstTime(), getPredTime(), getRoute(), getStopID(),
						getStopName(), getType(), getVid());
	}

}
